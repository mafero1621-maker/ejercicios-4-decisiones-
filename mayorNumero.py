# Entrada de datos
num1 = int(input("Escribe el num1: "))
num2 = int(input("Escribe el num2: "))

# Proceso y salida
if num1 > num2:
    print("El numero", num1, "es mayor que", num2)
elif num1 < num2:
    print("El numero", num1, "no es mayor que", num2)
else:
    print("Los numeros son iguales")
